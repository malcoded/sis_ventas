    private void tblClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientMouseClicked
        int row = tblClient.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = (int) tblClient.getValueAt(row, 0);
            String document = (String) tblClient.getValueAt(row, 1);
            String name = (String) tblClient.getValueAt(row, 2);
            String address = (String) tblClient.getValueAt(row, 3);
            boolean status = (boolean) tblClient.getValueAt(row, 4);
            setValuesForm(document, name, address, status, id);
        }
    }//GEN-LAST:event_tblClientMouseClicked

    private void chkStatusStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_chkStatusStateChanged
        boolean status = chkStatus.isSelected();
        if (status)
            chkStatus.setText("Activo");
        else
            chkStatus.setText("Inactivo");
    }//GEN-LAST:event_chkStatusStateChanged

    void addClient() {
        String document = txtDocument.getText();
        String name = txtName.getText();
        String address = txtAddress.getText();

        if ("".equals(document) || "".equals(name) || "".equals(address)) {
            JOptionPane.showMessageDialog(this, "Por favor, Ingres DNI, Nombres y Dirección");
        } else {
            boolean status = chkStatus.isSelected();
            Object[] obj = new Object[4];
            obj[0] = document;
            obj[1] = name;
            obj[2] = address;
            obj[3] = status;
            clientDao.add(obj);

            cleanTable();
            listClient();
            newClient();
        }

    }

    void updateClient() {
        int row = tblClient.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = clientEntity.getId();
            String document = txtDocument.getText();
            String name = txtName.getText();
            String address = txtAddress.getText();
            boolean status = chkStatus.isSelected();
            Object[] obj = new Object[5];
            obj[0] = document;
            obj[1] = name;
            obj[2] = address;
            obj[3] = status;
            obj[4] = id;
            clientDao.update(obj);
        }
    }

    void deleteClient() {
        int row = tblClient.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = clientEntity.getId();
            clientDao.delete(id);
        }
    }

    void newClient() {
        setValuesForm("", "", "", false, -1);
    }

    void cleanTable() {
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            tableModel.removeRow(i);
            i--;
        }
    }

    void setValuesForm(String document, String name, String address, boolean status, int id) {
        clientEntity.setId(id);
        txtDocument.setText(document);
        txtName.setText(name);
        txtAddress.setText(address);
        chkStatus.setSelected(status);
        chkStatus.setText(status ? "Activo" : "Inactivo");
        txtDocument.requestFocus();
    }

