    void listClient() {
        List<ClientEntity> list = clientDao.listRecords();
        tableModel = (DefaultTableModel) tblClient.getModel();
        Object[] obj = new Object[5];

        for (int i = 0; i < list.size(); i++) {
            obj[0] = list.get(i).getId();
            obj[1] = list.get(i).getDocument();
            obj[2] = list.get(i).getName();
            obj[3] = list.get(i).getAddress();
            obj[4] = list.get(i).isStatus();
            tableModel.addRow(obj);
        }
        tblClient.setModel(tableModel);
    }

