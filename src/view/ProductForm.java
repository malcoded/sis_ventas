    private void tblProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductsMouseClicked
        int row = tblProducts.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = (int) tblProducts.getValueAt(row, 0);
            String name = (String) tblProducts.getValueAt(row, 1);
            double price = (double) tblProducts.getValueAt(row, 2);
            int stock = (int) tblProducts.getValueAt(row, 3);
            boolean status = (boolean) tblProducts.getValueAt(row, 4);
            setValuesForm(name, price, stock, status, id);
        }
    }//GEN-LAST:event_tblProductsMouseClicked

    private void chkStatusStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_chkStatusStateChanged
        boolean status = chkStatus.isSelected();
        if (status)
            chkStatus.setText("Activo");
        else
            chkStatus.setText("Inactivo");
    }//GEN-LAST:event_chkStatusStateChanged

    void addProduct() {
        String name = txtName.getText().trim();
        String price = txtPrice.getText().trim();
        String stock = txtStock.getText().trim();

        if (("".equals(price)) || ("".equals(stock)) || ("".equals(name))) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese Nombre, Precio y Stock.");
        } else {
            boolean status = chkStatus.isSelected();
            Object[] obj = new Object[4];
            obj[0] = name;
            obj[1] = price;
            obj[2] = stock;
            obj[3] = status;
            productDao.add(obj);

            cleanTable();
            listProducts();
            newProduct();
        }

    }

    void updateProduct() {
        int row = tblProducts.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = productEntity.getId();
            String name = txtName.getText();
            String price = txtPrice.getText();
            String stock = txtStock.getText();
            boolean status = chkStatus.isSelected();

            Object[] obj = new Object[5];
            obj[0] = name;
            obj[1] = price;
            obj[2] = stock;
            obj[3] = status;
            obj[4] = id;
            productDao.update(obj);
        }
    }

    void deleteProduct() {
        int row = tblProducts.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = productEntity.getId();
            productDao.delete(id);
        }
    }

    void newProduct() {
        setValuesForm("", 0, 0, false, -1);
    }

    void cleanTable() {
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            tableModel.removeRow(i);
            i--;
        }
    }

    void setValuesForm(String name, double price, int stock, boolean status, int id) {
        productEntity.setId(id);
        txtName.setText(name);
        txtPrice.setText(price > 0 ? Double.toString(price) : "");
        txtStock.setText(stock > 0 ? Integer.toString(stock) : "");
        chkStatus.setSelected(status);
        chkStatus.setText(status ? "Activo" : "Inactivo");
        txtName.requestFocus();
    }

