    private void tblSellersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSellersMouseClicked
        int row = tblSellers.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = (int) tblSellers.getValueAt(row, 0);
            String document = (String) tblSellers.getValueAt(row, 1);
            String name = (String) tblSellers.getValueAt(row, 2);
            String phone = (String) tblSellers.getValueAt(row, 3);
            String username = (String) tblSellers.getValueAt(row, 4);
            boolean status = (boolean) tblSellers.getValueAt(row, 5);
            setValuesForm(document, name, phone, status, username, id);
        }
    }//GEN-LAST:event_tblSellersMouseClicked

    void addSeller() {
        String document = txtDocument.getText();
        String name = txtName.getText();
        String phone = txtPhone.getText();
        String username = txtUsername.getText();

        if ("".equals(document) || "".equals(name) || "".equals(phone) || "".equals(username)) {
            JOptionPane.showMessageDialog(this, "Por favor, Ingres DNI, Nombres, Teléfono y Nombre Usuario");
        } else {
            boolean status = chkStatus.isSelected();
            Object[] obj = new Object[5];
            obj[0] = document;
            obj[1] = name;
            obj[2] = phone;
            obj[3] = status;
            obj[4] = username;
            sellerDao.add(obj);

            cleanTable();
            listSellers();
            newSeller();
        }

    }

    void updateClient() {
        int row = tblSellers.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = sellerEntity.getId();
            String document = txtDocument.getText();
            String name = txtName.getText();
            String phone = txtPhone.getText();
            String username = txtUsername.getText();

            boolean status = chkStatus.isSelected();
            Object[] obj = new Object[6];
            obj[0] = document;
            obj[1] = name;
            obj[2] = phone;
            obj[3] = status;
            obj[4] = username;
            obj[5] = id;
            sellerDao.update(obj);
        }
    }

    void deleteClient() {
        int row = tblSellers.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila");
        } else {
            int id = sellerEntity.getId();
            sellerDao.delete(id);
        }
    }

    void newSeller() {
        setValuesForm("", "", "", false, "", -1);
    }

    void cleanTable() {
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            tableModel.removeRow(i);
            i--;
        }
    }

    void setValuesForm(String document, String name, String address, boolean status, String username, int id) {
        sellerEntity.setId(id);
        txtDocument.setText(document);
        txtName.setText(name);
        txtPhone.setText(address);
        txtUsername.setText(username);
        chkStatus.setSelected(status);
        chkStatus.setText(status ? "Activo" : "Inactivo");
        txtDocument.requestFocus();
    }

