    void listSellers() {
        List<SellerEntity> list = sellerDao.listRecords();
        tableModel = (DefaultTableModel) tblSellers.getModel();
        Object[] obj = new Object[6];

        for (int i = 0; i < list.size(); i++) {
            obj[0] = list.get(i).getId();
            obj[1] = list.get(i).getDocument();
            obj[2] = list.get(i).getName();
            obj[3] = list.get(i).getPhone();
            obj[4] = list.get(i).getUserName();
            obj[5] = list.get(i).isStatus();
            tableModel.addRow(obj);
        }

        tblSellers.setModel(tableModel);
    }

