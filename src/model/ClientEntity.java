package model;

/**
 *
 * @author developer1
 */
public class ClientEntity {

    int id;
    String document;
    String name;
    String address;
    boolean status;

    public ClientEntity() {
    }

    public ClientEntity(int id, String document, String name, String address, boolean status) {
        this.id = id;
        this.document = document;
        this.name = name;
        this.address = address;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDocument() {
        return document;
    }

    public void setDocument(String document) {
        this.document = document;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

}
